void set_number(uint8_t n, uint8_t r, uint8_t c)
{
    draw_graphic(&NUMBERS[n * 3], r, c, 1, 3, GRAPHIC_SET);
}

void set_number2(uint8_t n, uint8_t r, uint8_t c)
{
    if(n > 99) n = 99;
    uint8_t t = 0;
    while(n >= 10)
        n -= 10, ++t;
    if(t != 0)
    {
        set_number(t, r, c);
        set_number(n, r, c + 4);
    }
    else
        set_number(n, r, c + 2);
}

void set_number3(uint16_t n, uint8_t r, uint8_t c)
{
    if(n > 999) n = 999;
    uint8_t t0 = 0, t1 = 0;
    while(n >= 100)
        n -= 100, ++t0;
    while(n >= 10)
        n -= 10, ++t1;
    uint8_t x = c;
    if(t0 != 0)
    {
        set_number(t0, r, c);
        set_number(t1, r, c + 4);
        set_number(n, r, c + 8);
    }
    else if(t1 != 0)
    {
        set_number(t1, r, c + 2);
        set_number(n, r, c + 6);
    }
    else
        set_number(n, r, c + 4);
}

