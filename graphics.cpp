void draw_graphic(
    uint8_t const* p,
    uint8_t r, uint8_t c,
    uint8_t h, uint8_t w,
    uint8_t op)
{
    uint8_t* br = &buf[0] + r * FBW;
    for(uint8_t y = 0; y < h; ++y, br += FBW)
    {
        for(uint8_t x = 0; x < w; ++x)
        {
            uint8_t t = pgm_read_byte(p++);
            uint8_t bi = x + c;
            if(bi >= FBW) continue;
            uint8_t* bc = br + bi;
            if(op == GRAPHIC_OVERWRITE)
                *bc = t;
            else if(op == GRAPHIC_SET)
                *bc |= t;
            else
                *bc &= ~t;
        }
    }
}

